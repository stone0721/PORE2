def valid(x, y):
    if y % 3 == 0:
        return map1[x][y // 3] == '*'
    elif y % 3 == 1:
        return map2[32 - x][y // 3] == '*'
    elif y % 3 == 2:
        return map3[x][y // 3] == '*'
    return False


